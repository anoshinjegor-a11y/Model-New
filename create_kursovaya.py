def tnr(run, bold=False, size=14, italic=False):
    """Установить Times New Roman + CS для кириллицы."""
    run.font.name   = 'Times New Roman'
    run.font.size   = Pt(size)
    run.font.bold   = bold
    run.font.italic = italic
    rPr = run._r.get_or_add_rPr()
    rf  = rPr.find(qn('w:rFonts'))
    if rf is None:
        rf = OxmlElement('w:rFonts')
        rPr.insert(0, rf)
    for a in ('w:ascii', 'w:hAnsi', 'w:cs', 'w:eastAsia'):
        rf.set(qn(a), 'Times New Roman')
    return run

def _pf(para, indent=True, align=WD_ALIGN_PARAGRAPH.JUSTIFY,
        pgbrk=False, keep_next=False, left_indent=None):
    """Применить стандартное форматирование абзаца."""
    para.alignment = align
    pf = para.paragraph_format
    pf.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
    pf.space_before = Pt(0)
    pf.space_after  = Pt(0)
    pf.first_line_indent = Cm(1.25) if indent else Cm(0)
    if left_indent is not None:
        pf.left_indent = Cm(left_indent)
    if pgbrk:
        pf.page_break_before = True
    if keep_next:
        pf.keep_with_next = True
    return para

def body(doc, text):
    """Обычный абзац основного текста (п.4.9, отступ 1,25 см, выравнивание по ширине)."""
    p = doc.add_paragraph()
    _pf(p, indent=True)
    if text:
        tnr(p.add_run(text))
    return p

