def add_table(doc, headers, rows, col_widths=None):
    t = doc.add_table(rows=1 + len(rows), cols=len(headers))
    t.style = 'Table Grid'
    for i, h in enumerate(headers):
        cell = t.rows[0].cells[i]
        cell.text = ''
        p = cell.paragraphs[0]
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        p.paragraph_format.space_before = Pt(0)
        p.paragraph_format.space_after  = Pt(0)
        p.paragraph_format.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
        p.paragraph_format.first_line_indent = Cm(0)
        tnr(p.add_run(h), bold=True, size=12)
    for ri, row in enumerate(rows):
        for ci, val in enumerate(row):
            cell = t.rows[ri + 1].cells[ci]
            cell.text = ''
            p = cell.paragraphs[0]
            p.alignment = WD_ALIGN_PARAGRAPH.LEFT
            p.paragraph_format.space_before = Pt(0)
            p.paragraph_format.space_after  = Pt(0)
            p.paragraph_format.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
            p.paragraph_format.first_line_indent = Cm(0)
            tnr(p.add_run(val), size=12)
    if col_widths:
        for i, w in enumerate(col_widths):
            for row in t.rows:
                row.cells[i].width = Cm(w)
    return t

def add_auto_toc(doc):
    """Автособираемое содержание через поле Word TOC (п.4.29, Приложение В).
    После открытия файла нажать F9 или ПКМ → «Обновить поле»."""
    p = doc.add_paragraph()
    _pf(p, indent=False, align=WD_ALIGN_PARAGRAPH.LEFT)
    run = p.add_run()

    begin = OxmlElement('w:fldChar')
    begin.set(qn('w:fldCharType'), 'begin')
    begin.set(qn('w:dirty'),       'true')   # пометить поле как требующее обновления

    instr = OxmlElement('w:instrText')
    instr.set(qn('xml:space'), 'preserve')
    # \o "1-2" — стили Heading 1 и Heading 2
    # \h — гиперссылки на разделы
    # \z — скрывать страницы в веб-виде
    instr.text = ' TOC \\o "1-2" \\h \\z '

    sep = OxmlElement('w:fldChar')
    sep.set(qn('w:fldCharType'), 'separate')

    placeholder = OxmlElement('w:r')
    pt = OxmlElement('w:t')
    pt.text = '[Содержание обновится при открытии в Word: F9 или ПКМ → Обновить поле]'
    placeholder.append(pt)

    end = OxmlElement('w:fldChar')
    end.set(qn('w:fldCharType'), 'end')

    run._r.append(begin)
    run._r.append(instr)
    run._r.append(sep)
    run._r.append(placeholder)
    run._r.append(end)
    tnr(run, italic=True, size=12)
    return p

# ── ВСПОМОГАТЕЛЬНАЯ ДЛЯ ТИТУЛЬНОГО ЛИСТА ─────────────────────────────────────
def tp(text, bold=False, size=12, align=WD_ALIGN_PARAGRAPH.CENTER):
    p = doc.add_paragraph()
    p.alignment = align
    p.paragraph_format.space_before      = Pt(0)
    p.paragraph_format.space_after       = Pt(0)
    p.paragraph_format.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
    p.paragraph_format.first_line_indent = Cm(0)
    if text:
        tnr(p.add_run(text), bold=bold, size=size)
    return p

def tp_right(text, bold=False, size=12):
    return tp(text, bold=bold, size=size, align=WD_ALIGN_PARAGRAPH.RIGHT)

